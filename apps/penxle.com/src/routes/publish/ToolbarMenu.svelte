<script lang="ts">
  import { computePosition, flip, offset, shift } from '@floating-ui/dom';
  import { tick } from 'svelte';
  import { afterNavigate } from '$app/navigation';
  import { portal } from '$lib/svelte/actions';
  import ColorPaletteButton from './ColorPaletteButton.svelte';
  import ToolbarButton from './ToolbarButton.svelte';
  import type { Editor } from '@tiptap/core';

  export let editor: Editor | undefined;
  let targetEl: HTMLDivElement;
  let menuEl: HTMLDivElement;

  let open = false;

  let _class: string;
  export { _class as class };
  export let name: string;
  export let active = false;
  export let enabled = true;

  const update = async () => {
    await tick();

    const position = await computePosition(targetEl, menuEl, {
      placement: 'top-end',
      middleware: [offset(4), flip(), shift({ padding: 8 })],
    });

    Object.assign(menuEl.style, {
      left: `${position.x}px`,
      top: `${position.y}px`,
    });
  };

  $: if (open) {
    void update();
  }

  afterNavigate(() => {
    open = false;
  });
</script>

<div bind:this={targetEl} tabindex="-1">
  <ToolbarButton {name} class={_class} {active} {enabled} on:click={() => (open = true)} />
</div>

{#if open}
  <div
    class="fixed inset-0 z-49"
    role="button"
    tabindex="-1"
    on:click={() => (open = false)}
    on:keypress={null}
    use:portal
  />
  <div bind:this={menuEl} class="absolute z-50 w-64 flex flex-col border rounded bg-white py-2 shadow" use:portal>
    <ColorPaletteButton
      class="bg-brand-50"
      active={editor?.getAttributes('text-color')?.['data-text-color'] === 'text-brand-50'}
      enabled={editor?.can().setTextColor({ 'data-text-color': 'text-brand-50' })}
      on:click={() => {
        open = false;

        return editor?.chain().focus().setTextColor({ 'data-text-color': 'text-brand-50' }).run();
      }}
    />

    <ColorPaletteButton
      class="bg-gray-90"
      active={!editor?.isActive('text-color')}
      enabled={editor?.can().unsetTextColor()}
      on:click={() => {
        open = false;

        return editor?.chain().focus().unsetTextColor().run();
      }}
    />
  </div>
{/if}
